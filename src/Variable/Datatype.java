package Variable;

public class Datatype {
	
	public static void main(String[]args)
	{
		//Variable Delaration
		byte a;     //Data Type Variable name;
		short b;    //Data Type Variable name;
		int c;     //Data Type Variable name;
		long d;    //Data Type Variable name;
		float e;   //Data Type Variable name;
		double f;   //Data Type Variable name;
		char g;    //Data Type Variable name;
		boolean h;  //Data Type Variable name;
		boolean i;   //Data Type Variable name;
		
		//Variable Initialisation
		a=100;                   //variable name="Variable information/data";
		b=-32765;                //variable name="Variable information/data";
		c=2147483647;             //variable name="Variable information/data";
		d=8223567890123456799L;   //variable name="Variable information/data";
		e=10.2145454f;             //variable name="Variable information/data";
		f=20.55445464;            //variable name="Variable information/data";
		g='v';                   //variable name="Variable information/data";
		h=true;                  //variable name="Variable information/data";
		i=false;                 //variable name="Variable information/data";  
		
		//Usage
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i);
		
	
		
		
		
		
	}

}
